package StarProgramInJava;

public class StarPatternInJava {

	// The first for loop works for the row and the second loop works for the column.
	// In the pattern programs, Java for loop
	
	public static void main(String[] args) {

//		int i,j;
	//	int row=6;
	
		for (int i = 0; i < 6; i++)
		{
			for (int j = 0; j<=i ; j++)
				
//				For i=0, 0<4 (true)
//				For j=0, j<=0 (true)
				
			{
				System.out.print("*");
				
			}
			System.out.println();
			
		}
		
		
		
	}

}
