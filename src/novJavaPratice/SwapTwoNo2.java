public class SwapTwoNo2 {

	public static void main(String[] args) {

		int x=10;
		int y=20;
		int temp;
		System.out.println(x+","+y);
		
		temp=x;
		System.out.println(temp);
		x=y;
		System.out.println(x);
		y=temp;
				
		System.out.println(x);
		System.out.println(y);
		
		
	}

}
